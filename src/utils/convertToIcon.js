// icons thanks to devicon.dev

export default function convertToIcon(name) {
    if (name === "html") {
        return "devicon-html5-plain-wordmark plain"
    } 
    else if (name === "css") {
        return "devicon-css3-plain-wordmark plain"
    } 
    else if (name === "vanillaJS") {
        return ("devicon-javascript-plain plain")
    } 
    else if (name === "python") {
        return ("devicon-python-plain plain")
    }
    else if (name === "django") {
        return ("devicon-django-plain plain")
    }
    else if (name === "react") {
        return ("devicon-react-original")
    }
    else if (name === "bootstrap") {
        return ("devicon-bootstrap-plain")
    }
    else if (name === "sqlite") {
        return ("devicon-sqlite-plain")
    }
    

}

